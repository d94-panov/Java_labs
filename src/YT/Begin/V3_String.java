package YT.Begin;

public class V3_String {
    public static void main(String[] args) {
        int x = 5;
        String s = "Hello"; // String - это класс, поэтому пишется с большой буквы
        String space = " ";
        String name = "Bob";
        System.out.println(s+space+name); // объединяем строки
        System.out.println("Hello" + " "+ "John"); // объединяем строки и добавляем свой текст
        System.out.println("My number is "+ x);
    }
}
